/* commonTypes.h */

#ifndef COMMONTYPES_H
#define COMMONTYPES_H

/* Common Typedefs */
/* Unsigned Integers */
typedef unsigned char uint8_t;         /* 1-Byte integer */
typedef unsigned short int uint16_t;   /* 2-Byte integer */
typedef unsigned long int uint32_t;    /* 4-Byte integer */

/* Signed Integers */
typedef signed char int8_t;           /* 1-Byte integer */
typedef signed short int int16_t;     /* 2-Byte integer */
typedef signed long int int32_t;      /* 4-Byte integer */

#endif /* End of commonTypes.h */